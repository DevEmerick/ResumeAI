interface I18nContextProps {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, fallback: string) => string;
}

const I18nContext = createContext<I18nContextProps | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("pt");

  useEffect(() => {
    // Puxa do localStorage ou descobre pela linguagem do navegador
    const stored = localStorage.getItem("locale") as Locale;
    if (stored === "en" || stored === "pt") setLocaleState(stored);
    else setLocaleState(navigator.language.startsWith("pt") ? "pt" : "en");
  }, []);

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    localStorage.setItem("locale", l);
  };

  const t = (key: string, fallback: string) => (dictionaries[locale] as any)[key] || fallback;

  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>;
}

export function useTranslation() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useTranslation deve ser usado dentro de um I18nProvider");
  return ctx;
}